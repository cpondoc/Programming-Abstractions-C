/* Sum Odd Numbers */
#include <iostream>
using namespace std;

int main() {
	
	// Important local variables
	int n;
	
	// Header for output and input
	cout << "This program calculates the sum of the first n odd numbers." << endl;
	cout << "Input a value of n: ";
	cin >> n;
	
	// Calculation and return of output
	cout << "The sum of the first n odd integers is: " << n * n << endl;
	
	return 0;
}
